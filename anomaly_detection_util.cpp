/**
 * return the average of the array.
 * @param x is an array of numbers
 * @param size of the array.
 * @return the average.
 */
float avg(float* x, int size) {
    float sum;
    for (i = 0 ; i < size; i++) {
        sum += x[i];
    }
    return sum/size;
}

